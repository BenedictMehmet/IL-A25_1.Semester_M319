        /* Übung 1: Altersprüfung
         *
         * 1. Frage den Benutzer nach seinem Alter.
         * 2. Wenn das Alter >= 18, gib aus "Du bist volljährig."
         * 3. Wenn das Alter < 18, gib aus "Du bist minderjährig."
         * 4. Bonus: Prüfe auf negative Zahlen und gib eine Warnung aus.
         */


        /* Übung 2: Notenbewertung
         *
         * 1. Frage den Benutzer nach einer Punktzahl (0-100).
         * 2. Gib die Note nach folgendem Schema aus:
         *    90-100 -> "Sehr gut"
         *    75-89  -> "Gut"
         *    50-74  -> "Befriedigend"
         *    0-49   -> "Nicht bestanden"
         * 3. Bonus: Prüfe, ob die Punktzahl ausserhalb des gültigen Bereichs liegt.
         */

        /* Übung 3: Temperaturcheck
         *
         * 1. Frage den Benutzer nach der aktuellen Temperatur(in °C).
         * 2. Gib je nach Temperatur eine Nachricht aus:
         *    <0  -> "Es ist sehr kalt."
         *    0-15 -> "Es ist kühl."
         *    16-25 -> "Es ist angenehm."
         *    >25 -> "Es ist warm."
         * 3. Bonus: Prüfe auf unrealistische Werte z.B. <-100 oder >60.
         */
    }
